package com.example.demo.entity;

/**
 *
 * @author 84918
 */
public class BookTop {
    private Book book;
    private int count;

    public BookTop(Book book, int count) {
        this.book = book;
        this.count = count;
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
    
}
